const processInputData = function(data) {
  const dataArray = data.split("\n");
  return dataArray;
}

const submarineMoveForward = function(distanceUnits, position) {
  return position + distanceUnits;
}

const submarineMoveUp = function(distanceUnits, depth) {
  return depth - distanceUnits;
}

const submarineMoveDown = function(distanceUnits, depth) {
  return depth + distanceUnits;
}

const submarineMovements = function(data, depth, horizontalPosition) {
  for (item of data) {
    let distanceMoved = +item[item.length - 1];
    if (item.includes("forward")) {
      horizontalPosition = submarineMoveForward(distanceMoved, horizontalPosition);      
    } else if (item.includes("up")) {
      depth = submarineMoveUp(distanceMoved, depth);
    } else if (item.includes("down")) {
      depth = submarineMoveDown(distanceMoved, depth);
    }
  }
  return [horizontalPosition, depth]
   
}

const computeSubmarinePosition = function(inputData) {
  let processedData = processInputData(inputData);
  let depth = 0;
  let horizontalPosition = 0;
  
  [horizontalPosition, depth] = submarineMovements(processedData, depth, horizontalPosition);
  
  return horizontalPosition * depth;
}

